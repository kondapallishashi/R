#This program demonstrates different data types supported by R
#<- can be used to assign any value to a variable
a<-10
# = can also be used to assign values to the variables
b=15
#To print the variable, simply use the variable
a
b
#to see the variable type, use class(variable_name)
class(a)
#Decimal numbers are also supported
c<-1.5
class(c)
#Logical or boolean values TRUE, FALSE are supported
d<-TRUE
d
class(d)
e<-"Characters or strings are enclosed in double quotes or single quotes"
e
f<-'String in single quotes'
f
class(e)
